package SedtJavaPrograms;

public class Operators {

	public static void main(String[] args) {
		

		int a = 10;
		int b = 5;
		
		//Arithmetic operator
		// +,-,*,/,%
		
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		
		//Relation operator
		// >,<,>=,<=,==,!=
		//Return to  boolean variable True/False
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
		//Logical operator
		//and && , or ||,not!
	    //boolean varaiable
		
		boolean x = true;
		boolean y = false;
		
		System.out.println(x&&y);
		System.out.println(x||y);
		System.out.println(!x);
		System.out.println(!y);

		
		//incremental operator & Assignment operator
		
		int c;
		c=100;
		//c=c+1;
		//c++;
		
		//c=c+5;
		//c+=5;
		
		System.out.println(c);
		
		//decremental operator & assignment operator
		
		//c=c-1;
		//c--;
		
		//c=c-5;
		c-=5;
		
		System.out.println(c);
		
		
		
		
		
}
}
